// ============================================
// SEED FUNCTION
// ============================================

async function seed() {
  console.log(' Starting database seed...\n');

  try {
    // Clear existing data
    console.log(' Clearing existing data...');
    await prisma.phaseToolRecommendation.deleteMany();
    await prisma.bundleTool.deleteMany();
    await prisma.toolBundle.deleteMany();
    await prisma.toolReplacement.deleteMany();
    await prisma.toolRedundancy.deleteMany();
    await prisma.toolIntegration.deleteMany();
    await prisma.tool.deleteMany();
    console.log(' Cleared existing data\n');

    // Seed tools
    console.log(' Seeding tools...');
    const toolMap = new Map<string, string>();

    for (const tool of tools) {
      const created = await prisma.tool.create({
        data: tool as any,
      });
      toolMap.set(tool.name, created.id);
      console.log(`   ${tool.displayName}`);
    }
    console.log(` Seeded ${tools.length} tools\n`);

    // Seed redundancies
    console.log(' Seeding redundancies...');
    let redundancyCount = 0;
    for (const r of redundancies) {
      const toolAId = toolMap.get(r.toolA);
      const toolBId = toolMap.get(r.toolB);

      if (toolAId && toolBId) {
        await prisma.toolRedundancy.create({
          data: {
            toolAId,
            toolBId,
            overlappingUseCases: r.overlappingUseCases,
            overlappingFeatures: r.overlappingFeatures,
            redundancyStrength: r.redundancyStrength as any,
            recommendationHint: r.recommendationHint as any,
            notes: r.notes || null,
          },
        });
        redundancyCount++;
      }
    }
    console.log(` Seeded ${redundancyCount} redundancies\n`);

    // Seed replacements (dedupe by from+to)
    console.log(' Seeding replacements...');
    let replacementCount = 0;
    const seenReplacements = new Set<string>();
    for (const r of replacements) {
      const fromToolId = toolMap.get(r.from);
      const toToolId = toolMap.get(r.to);
      const key = `${r.from}->${r.to}`;

      if (fromToolId && toToolId && !seenReplacements.has(key)) {
        seenReplacements.add(key);
        await prisma.toolReplacement.create({
          data: {
            fromToolId,
            toToolId,
            reasonType: r.reasonType as any,
            reasonText: r.reasonText,
            context: r.context || null,
            conditions: r.conditions || null,
          },
        });
        replacementCount++;
      }
    }
    console.log(` Seeded ${replacementCount} replacements\n`);

    // Seed bundles
    console.log(' Seeding bundles...');
    for (const bundle of bundles) {
      const anchorToolId = bundle.anchorToolName ? toolMap.get(bundle.anchorToolName) : null;

      // Convert tool roles to IDs
      const toolRolesWithIds: Record<string, string> = {};
      for (const [name, role] of Object.entries(bundle.toolRoles)) {
        const toolId = toolMap.get(name);
        if (toolId) {
          toolRolesWithIds[toolId] = role;
        }
      }

      const createdBundle = await prisma.toolBundle.create({
        data: {
          name: bundle.name,
          description: bundle.description,
          toolRoles: toolRolesWithIds,
          primaryUseCasesCovered: bundle.primaryUseCasesCovered,
          bestForTeamSize: bundle.bestForTeamSize as any,
          bestForStage: bundle.bestForStage as any,
          bestForTechSavviness: bundle.bestForTechSavviness as any,
          anchorToolId,
          scenarioType: bundle.scenarioType as any,
          estimatedMonthlyCost: bundle.estimatedMonthlyCost,
        },
      });

      // Create bundle-tool associations
      for (const toolName of bundle.toolNames) {
        const toolId = toolMap.get(toolName);
        if (toolId) {
          await prisma.bundleTool.create({
            data: {
              bundleId: createdBundle.id,
              toolId,
              role: bundle.toolRoles[toolName] || null,
            },
          });
        }
      }

      console.log(`   ${bundle.name}`);
    }
    console.log(` Seeded ${bundles.length} bundles\n`);

    // Seed phase recommendations
    console.log(' Seeding phase recommendations...');
    let phaseCount = 0;
    for (const rec of phaseRecommendations) {
      const toolId = toolMap.get(rec.toolName);

      if (toolId) {
        await prisma.phaseToolRecommendation.create({
          data: {
            phase: rec.phase as any,
            toolId,
            role: rec.role,
            techSavviness: rec.techSavviness as any,
            note: rec.note || null,
            displayOrder: rec.displayOrder,
          },
        });
        phaseCount++;
      }
    }
    console.log(` Seeded ${phaseCount} phase recommendations\n`);

    console.log(' Database seeding complete!\n');
    console.log('Summary:');
    console.log(`   ${tools.length} tools`);
    console.log(`   ${redundancyCount} redundancy pairs`);
    console.log(`   ${replacementCount} replacement rules`);
    console.log(`   ${bundles.length} bundles`);
    console.log(`   ${phaseCount} phase recommendations`);

  } catch (error) {
    console.error(' Error seeding database:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

// Run seed
seed()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  });
